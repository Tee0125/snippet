void get_segment2( bitmap* src, int x, int y, char** ch, char** checked ){

	int i, j;
	int pixel;

	if( x < 0 || y < 0 || x >= src->width || y >= src->height )
		return;

	if( checked[y][x] != 0 )
		return;

	checked[y][x] = 1;
	if( get_bit(src, x, y) > THRESHOLD )
		return;

	ch[y][x] = 1;

	if( x > segment_max_x )
		segment_max_x = x;
	if( x < segment_min_x )
		segment_min_x = x;
	if( y > segment_max_y )
		segment_max_y = y;
	if( y < segment_min_y )
		segment_min_y = y;

	get_segment2( src, x-1, y-1, ch, checked );
	get_segment2( src, x,   y-1, ch, checked );
	get_segment2( src, x+1, y-1, ch, checked );
	get_segment2( src, x-1, y,   ch, checked );
	get_segment2( src, x+1, y,   ch, checked );
	get_segment2( src, x-1, y+1, ch, checked );
	get_segment2( src, x,   y+1, ch, checked );
	get_segment2( src, x+1, y+1, ch, checked );

}

