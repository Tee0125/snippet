// 크기 정규화
bitmap* scale_normalize( bitmap* src ){

	int i, j;
	int r = 1;
	
	int x, y;
	int var;
	
	double ratio;
	
	image* im1;
	image* im2;
	
	bitmap* dst;
	
	// 글자가 내접하는 가장 큰 원의 반지름 r 을 구함
	get_center( src, &x, &y );
	for( j = 0 ; j < bitmap_y(src) ; j++ ){
		for( i = 0 ; i < bitmap_x(src) ; i++ ){
			bitmap_get( src, i, j, &var );

			if( var > THRESHOLD && distance(i-x,j-y) > r )
				r = distance(i-x,j-y);
		}
	}
	ratio = NORMALIZED_SIZE / (double)r;
	 
	// r:normalized_size 비율로 축소/확대
	im1 = convert_b2i(src);
	im2 = image_create( ceil(bitmap_x(src)*ratio), ceil(bitmap_y(src)*ratio) );
	
	gdImageCopyResampled(im2, im1, 
	                   0, 0, 0, 0, 
	                   image_x(im2), image_y(im2),
	                   image_x(im1), image_y(im1)
				);
	
	dst = convert_i2b(im2);

	image_destroy(im2);
	image_destroy(im1);
	
	return dst;
	
}


