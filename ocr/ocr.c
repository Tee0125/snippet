// 무게 중심 구하기
void get_center( bitmap* src, int* x, int* y ){

	int i, j;
	int m00, m01, m10;
	int var;
	
	m00 = m01 = m10 = 0;
	for( j = 0 ; j < src->height ; j++ ){
		for( i = 0 ; i < src->width ; i++ ){
			bitmap_get( src, i, j, &var );

			m00 += var;
			m10 += var * i;
			m01 += var * j;
		}
	}
	
	if( m00 == 0 ){
		(*x) = bitmap_x(src) / 2;
		(*y) = bitmap_y(src) / 2;
	}
	else {
		(*x) = m10 / m00;
		(*y) = m01 / m00;
	}
	
}

