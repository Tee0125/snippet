int match_simple_( bitmap* src, bitmap* dst ){

	int i, j, i_e, j_e;
	int match, count;
	
	int d;
	
	int s_x_, s_y_;
	int d_x_, d_y_;
	
	get_center( src, &s_x_, &s_y_ );
	get_center( dst, &d_x_, &d_y_ );

	i = -1 * get_max( s_x_, d_x_ );
	i_e = get_max( abs(src->width - s_x_), abs(dst->width - d_x_) );
	
	j = -1 * get_max( s_y_, d_y_ );
	j_e = get_max( abs(src->height - s_y_), abs(dst->height - d_y_) );
	
	match = count = 0;
	for( ; j < j_e ; j++ ){
		for( ; i < i_e ; i++ ){
			d = get_bit(src,s_x_+i, s_y_+j) - get_bit(dst, d_x_+i,d_y_+j);
			match += (abs(d) < THRESHOLD) ? 0: 1;
			count++;
		}
	}
	return (100 * match / count);
	
}

int match_ring_( ring_ptr src, ring_ptr dst ){
	int i, end;	
	int d, diff = 0;
		
	end = (src->length > dst->length)?src->length: dst->length;
	for( i = 0 ; i < end ; i++ ){
		if( i < src->length )
			d = src->r[i];
		else
			d = 0;
			
		if( i < dst->length )
			d -= dst->r[i];

		diff += abs(d);
	}
	
	return diff;
}

