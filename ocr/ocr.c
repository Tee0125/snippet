void segmentation( bitmap* src ){
	char** ch;
	char** checked;

	int i, j;
	int pixel;

	ch = (char**)malloc( sizeof(char*)*src->height );
	checked = (char**)malloc( sizeof(char*)*src->height );
	for( j = 0 ; j < src->height ; j++ ){
		ch[j] = (char*)malloc( sizeof(char)*src->width );
		memset( ch[j], 0, src->width );

		checked[j] = (char*)malloc( sizeof(char)*src->width );
		memset( checked[j], 0, src->width );
	}
	
	for( j = 0 ; j < src->height ; j++ )
		for( i = 0 ; i < src->width ; i++ )
			if( checked[j][i] == 0 ){
				if( get_bit(src, i, j) <= THRESHOLD )
					get_segment( src, i, j, ch, checked );
			}

	for( j = 0 ; j < src->height ; j++ ){
		free(ch[j]);
		free(checked[j]);
	}
}

